package gpig.group2.imageui.model;

public class StrandedPersonImage extends StrandedPersonPoi {

	private int id;
	private boolean yes;
	private boolean no;

	public int getId() {

		return id;
	}

	public void setId(int id) {

		this.id = id;
	}

	public boolean isYes() {

		return yes;
	}

	public void setYes(boolean yes) {

		this.yes = yes;
	}

	public boolean isNo() {

		return no;
	}

	public void setNo(boolean no) {

		this.no = no;
	}
}